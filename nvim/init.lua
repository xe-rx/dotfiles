local function set_whitespace_highlighting()
  local bufname = vim.fn.bufname()
  if bufname == "" or vim.bo.filetype == "dashboard" then
    vim.cmd("match none")
  else
    vim.cmd("match ExtraWhitespace /\\s\\+$/")
  end
end

